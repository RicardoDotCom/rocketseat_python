# Inteiro
numero_inteiro = 3
print("Inteiro = ", numero_inteiro) 

# Real com ponto flutuante
numero_real = 3.14
print("Real com ponto flutuante = ", numero_real)

# type() função que tem como objetivo retornar o tipo de uma variavel
print("Tipo da varivale inteiro = ", type(numero_inteiro))
print("Tipo da varivale real = ", type(numero_real))

# soma 
soma = 1 + 1
print("1 + 1 = ",soma)

# subtração
subtracao = 1 - 1
print("1 - 1 = ", subtracao)

# multiplicação
multiplicacao = 2 * 2 
print("2 x 2 = ", multiplicacao)

# divisão sempre que usarmos a divisão será um float 
divisao = 5 / 2 
print("5 / 2 = ", divisao)
print("Tipo da variavel do resultado da divisão = ", type(divisao))

# int()
print("5 / 2 em inteiro = ", int(divisao))

# float()
print("soma em float = ", float(soma))

# divisão com o tipo inteiro precisamos colocar //
divisao_inteiro = 5 // 2 
print("5 // 2 = ", divisao_inteiro)
print("Tipo da variavel do resultado da divisão = ", type(divisao_inteiro))

# módulo é o resto da divisão e é representado por %
modulo = 5 % 2 
print("O resto da divisão 5 / 2 = ", modulo)